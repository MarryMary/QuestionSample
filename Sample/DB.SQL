CREATE DATABASE DEVAPP;
USE DEVAPP;

CREATE TABLE Exercise(
    ExId BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    ExName VARCHAR(255) NOT NULL,
    ExDetail VARCHAR(255) NOT NULL,
    ExTags VARCHAR(500) NOT NULL,
    ExTimeUp VARCHAR(8) NOT NULL
);

CREATE TABLE Question(
    QId Int NOT NULL AUTO_INCREMENT PRIMARY KEY,
    QName VARCHAR(255) NOT NULL,
    QMain TEXT NOT NULL,
    AnswerArea VARCHAR(255) NOT NULL,
    QAnswer VARCHAR(500) NOT NULL,
    IncExId BIGINT NOT NULL
);

CREATE TABLE Respondent(
    ManageId BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
    UId BIGINT NOT NULL,
    QuestionId BIGINT NOT NULL,
    Score INT NOT NULL,
    CrWr INT NOT NULL,
    RecordedAt DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
);